public class LinguagemProgramacao {
 
    // Atributos
    String nome;
    int anosExperiencia;
    int ranking; // 1 = favorita, 2 = segunda, 3 = terceira
 
    // Construtor
    public LinguagemProgramacao(String nome, int anosExperiencia, int ranking) {
        this.nome = nome;
        this.anosExperiencia = anosExperiencia;
        this.ranking = ranking;
    }
}
 